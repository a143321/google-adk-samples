import os
import google.auth
from google.adk.agents import Agent
from google.adk.models import Gemini
from google.adk.tools import google_search
from google.adk.tools.agent_tool import AgentTool
from google.genai import types

# GCPプロジェクトおよびリージョンの自動設定（ADK標準の初期化）
_, project_id = google.auth.default()
os.environ["GOOGLE_CLOUD_PROJECT"] = project_id
os.environ["GOOGLE_CLOUD_LOCATION"] = os.environ.get(
    "GOOGLE_CLOUD_LOCATION", "asia-northeast1"
)
os.environ["GOOGLE_GENAI_USE_VERTEXAI"] = "True"

# ==============================================================================
# 1. scoring_agent (5S評価エージェント)
# ==============================================================================
scoring_agent = Agent(
    name="scoring_agent",
    model=Gemini(
        model="gemini-2.5-flash",
        retry_options=types.HttpRetryOptions(attempts=8),
    ),
    description="部屋やデスクの物理的な状態（散らかり具合や汚れなど）をテキストで受け取り、5S（整理・整頓・清掃・清潔・躾）の観点で各項目を10点満点で採点し、理由を出力する。",
    instruction="""提供された部屋・デスクの状態（テキスト）をもとに、5S（整理・整頓・清掃・清潔・躾）の各項目について10点満点で採点してください。
各項目について、スコア（0〜10）と、その点数にした理由（減点理由など）を明確に出力してください。

【評価基準の目安】
- 整理 (必要なものと不要なものが分けられ、不要なものがないか)
- 整頓 (必要なものが決められた場所に置かれ、すぐに取り出せるか)
- 清掃 (ゴミや汚れがなく、綺麗に掃除されているか)
- 清潔 (整理・整頓・清掃が維持され、衛生的な状態か)
- 躾 (ルールが守られ、習慣化されていそうな状態か)

出力例:
- 整理: 6/10 (机の右側に不要と思われる書類が山積みになっているため減点)
- 整頓: ...
""",
)

# ==============================================================================
# 2. search_agent (改善案検索エージェント)
# ==============================================================================
search_agent = Agent(
    name="search_agent",
    model=Gemini(
        model="gemini-2.5-flash",
        retry_options=types.HttpRetryOptions(attempts=8),
    ),
    description="低スコアだった5S項目の改善アイデア（収納グッズ、片付けテクニックなど）をGoogle検索で調べる。引数には「デスク ケーブル 収納術」などの検索キーワードを渡す。",
    instruction="""ユーザーから指定されたキーワード（例: 「書類 整理 アイデア」「デスク ケーブル 収納」）を使ってGoogle検索を行い、具体的な改善アイデアやおすすめグッズを3つほど提案してください。
必ず検索結果に基づいた具体的な情報（方法、グッズ名、参考になりそうなアプローチなど）を含めてください。""",
    tools=[google_search],
)

# ==============================================================================
# 3. report_agent (レポート統合エージェント)
# ==============================================================================
report_agent = Agent(
    name="report_agent",
    model=Gemini(
        model="gemini-2.5-flash",
        retry_options=types.HttpRetryOptions(attempts=8),
    ),
    description="画像解析結果、5Sスコア、検索された改善アイデアのすべてを受け取り、最終的なMarkdown形式の綺麗なレポートを生成する。",
    instruction="""提供された「画像解析で検出された問題点」「5Sのスコアと理由」「検索による改善アドバイス」をすべて統合し、以下のフォーマットに沿って綺麗なMarkdownレポートを生成してください。

# 🏠 5S 検査レポート
📅 検査日: (今日の日付)

## 総合スコア: (合計点)/50

| 5S 項目 | スコア | 評価 (例: ✅ 良好, ⚠️ 要改善, ❌ 要対応) |
|--------|-------|----------|
| 整理   |  X/10 | ... |
(※5項目すべてリスト化してください)

## 🔍 検出された問題と理由
(スコアリングエージェントが挙げた減点理由などをわかりやすく箇条書きで記載)

## 💡 改善アドバイス
(検索エージェントが提案した具体的なアイデアやグッズを箇条書きで記載。Web検索の情報であることを明示する)

## 📌 今週のアクション Top 3
(上記を踏まえて、今すぐできる具体的なTodoリストを優先度順に3つ、チェックボックス形式 `- [ ]` で出力)

※ 注意: マークダウンの表（Table）やリストを崩さないよう出力してください。
""",
)

# ==============================================================================
# 4. root_agent (Vision & オーケストレーター)
# ==============================================================================
root_agent = Agent(
    name="room_5s_agent",
    model=Gemini(
        model="gemini-2.5-pro",
        retry_options=types.HttpRetryOptions(attempts=8),
    ),
    description="部屋やデスクの画像を受け取り、自ら画像解析を行って問題点を抽出し、他のエージェントと協調して5Sレポートを作成する親エージェント。",
    instruction="""あなたはプロの5S（整理・整頓・清掃・清潔・躾）インスペクターです。
ユーザーから送信された画像（とテキスト）を受け取ったら、必ず以下の手順で処理を進めてください。

【協調エージェントの手順】
1. **画像解析（Vision）**: まず、あなた自身で画像を詳細に観察し、部屋やデスクにある物の配置、散乱状況、汚れ、危険箇所などの物理的な状態を客観的かつ詳細にテキストとしてリストアップしてください。
2. **スコアリング依頼**: 手順1で抽出した状態テキストを `scoring_agent` に渡し、5Sの各項目（10点満点）の採点と理由を出させてください。
3. **改善案の検索依頼**: スコアが低かった項目（例えば7点未満など）の減点理由をもとに、改善に役立つキーワード（例: 「デスク ケーブル 整理」）を考え、`search_agent` に渡して具体的な改善アイデアを検索させてください。
4. **レポート生成依頼**: 手順1の画像解析結果、手順2のスコア、手順3の改善アイデアをすべてまとめて `report_agent` に渡し、最終的なMarkdownレポートを生成させてください。
5. **最終回答**: `report_agent` が生成したMarkdownレポートを、そのままユーザーへの最終回答として出力してください。

【重要】
- ツール呼び出しの間、ユーザーに「今から〇〇を調べます」などの途中経過（中間応答）や思考プロセスは一切出力しないでください。最終的なレポートのみを回答してください。
- ユーザーから画像が送られていない場合は、「5Sチェックを行うため、部屋やデスクの画像をアップロードしてください。」と優しく案内してください。
""",
    tools=[
        AgentTool(agent=scoring_agent),
        AgentTool(agent=search_agent),
        AgentTool(agent=report_agent),
    ],
)

from google.adk.apps import App

app = App(
    root_agent=root_agent,
    name="app",
)
